				<span class="header"><center>STUDENT ALUMNI MENTORSHIP PROGRAMME</center></span><br />
				<span class="indent">&nbsp;</span>
				<p>Roorkee has a strong alumni base with our alumni carving a niche for themselves in whichever field they are working in and at whichever place they are settled. The "<b><i>Student Alumni Mentorship Programme</i></b>" is being initiated with the aim to <b>inspire, inform, guide, encourage and nurture</b> the young students thereby making a profound difference on their life and become a vital part of strengthening students' success. It is designed to help current students to cultivate a network of contacts in the professional community. Through mentorship, students have the opportunity to engage in meaningful career and personal growth exploration with experienced and knowledgeable alumni. At the same time, alumni have the opportunity to give back meaningfully to their alma mater by helping today's generation of students reach their personal and professional goals. The alumni also have the opportunity to network, interact, exchange ideas, and share experiences.</p>
				<p>The mission of the program is to:<p>
				<ul>
					<li>Offer alumni an opportunity to guide the career of students.</li>
					<li>Foster learning through the experience and expertise of the alumni.</li>
					<li>Encourage students' to explore all of their options by sharing information about professional opportunities and helping to develop their networking, interviewing and job search skills.</li>
					<li>Inspire the leaders of tomorrow to discover their talents and abilities and equipping them with the confidence they need to achieve their goals.</li>
					<li>Guide and nurture them by extending help in settling down at a new place, country or environment</li>
				</ul>
				<p>
				The "<b><i>Student-Alumni Mentorship Program</i></b>" is ideal for students who are looking for insight and direction on how to pursue their chosen career path. it's a unique way for them to engage in personal and professional discovery with someone who is supportive and has been in their shoes. As mentors you have the opportunity to provide your student mentee(s) with invaluable lessons on finding a work-life balance and setting goals for their career. Besides, you can also help them by giving them insight into effective resume writing, job searching, interviewing and dealing with workplace challenges.</p>
				<p>Initially, the mentorship programme is being launched for the pre-final and final year students which will progress further to the point where the mentor can make specific career suggestions and potentially help the student forge additional professional relationships. Alumni mentors will serve as role models and personal career advisors to the students. By openly sharing their experiences and what it takes to achieve success, mentors can truly make a difference in student's lives. The success of the programme depends a lot on the alumni's vital inputs, time and guidance to the students.<p>
